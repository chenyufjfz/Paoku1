    public AnimationClip create_move(Movement move, float speed = 60)
    {
        AnimationClip clip = new AnimationClip();

        for (int i = 0; i < GenerateBone.VALID_PART; i++)
        {
            AnimationCurve curve_w = new AnimationCurve();
            AnimationCurve curve_x = new AnimationCurve();
            AnimationCurve curve_y = new AnimationCurve();
            AnimationCurve curve_z = new AnimationCurve();
            List<Keyframe> keyframe_x = new List<Keyframe>();
            List<Keyframe> keyframe_y = new List<Keyframe>();
            List<Keyframe> keyframe_z = new List<Keyframe>();
            List<Keyframe> keyframe_w = new List<Keyframe>();
            float[,] part_move;
            switch (i)
            {
                case GenerateBone.HIP:
                    part_move = (move == Movement.RUN) ? hip_rot_run : ((move == Movement.JUMP) ? hip_rot_jump : hip_rot_roll);
                    break;
                case GenerateBone.LEFTUP_LEG:
                    part_move = (move == Movement.RUN) ? leftup_leg_rot_run : ((move == Movement.JUMP) ? leftup_leg_rot_jump : leftup_leg_rot_roll);
                    break;
                case GenerateBone.RIGHTUP_LEG:
                    part_move = (move == Movement.RUN) ? rightup_leg_rot_run : ((move == Movement.JUMP) ? rightup_leg_rot_jump : rightup_leg_rot_roll);
                    break;
                case GenerateBone.LEFT_LEG:
                    part_move = (move == Movement.RUN) ? left_leg_rot_run : ((move == Movement.JUMP) ? left_leg_rot_jump : left_leg_rot_roll);
                    break;
                case GenerateBone.RIGHT_LEG:
                    part_move = (move == Movement.RUN) ? right_leg_rot_run : ((move == Movement.JUMP) ? right_leg_rot_jump : right_leg_rot_roll);
                    break;
                case GenerateBone.SPINE:
                    part_move = (move == Movement.RUN) ? spine_rot_run : ((move == Movement.JUMP) ? spine_rot_jump : spine_rot_roll);
                    break;
                case GenerateBone.LEFT_ARM:
                    part_move = (move == Movement.RUN) ? left_arm_rot_run : ((move == Movement.JUMP) ? left_arm_rot_jump : left_arm_rot_roll);
                    break;
                case GenerateBone.RIGHT_ARM:
                    part_move = (move == Movement.RUN) ? right_arm_rot_run : ((move == Movement.JUMP) ? right_arm_rot_jump : right_arm_rot_roll);
                    break;
                case GenerateBone.LEFTFORE_ARM:
                    part_move = (move == Movement.RUN) ? leftfore_arm_rot_run : ((move == Movement.JUMP) ? leftfore_arm_rot_jump : leftfore_arm_rot_roll);
                    break;
                case GenerateBone.RIGHTFORE_ARM:
                    part_move = (move == Movement.RUN) ? rightfore_arm_rot_run : ((move == Movement.JUMP) ? rightfore_arm_rot_jump : rightfore_arm_rot_roll);
                    break;
                default:
                    part_move = (move == Movement.RUN) ? neck_run : ((move == Movement.JUMP) ? neck_jump : neck_roll);
                    break;
            }
            for (int j = 0; j < part_move.GetLength(0); j++)
            {
                Quaternion rot = Quaternion.identity;
                rot.eulerAngles = new Vector3(part_move[j, 0], part_move[j, 1], part_move[j, 2]);
                keyframe_w.Add(new Keyframe(j / speed, rot.w));
                keyframe_x.Add(new Keyframe(j / speed, rot.x));
                keyframe_y.Add(new Keyframe(j / speed, rot.y));
                keyframe_z.Add(new Keyframe(j / speed, rot.z));
            }
            curve_w.keys = keyframe_w.ToArray();
            clip.SetCurve(GenerateBone.RelativeBoneName[i], typeof(Transform), "m_LocalRotation.w", curve_w);
            curve_x.keys = keyframe_x.ToArray();
            clip.SetCurve(GenerateBone.RelativeBoneName[i], typeof(Transform), "m_LocalRotation.x", curve_x);
            curve_y.keys = keyframe_y.ToArray();
            clip.SetCurve(GenerateBone.RelativeBoneName[i], typeof(Transform), "m_LocalRotation.y", curve_y);
            curve_z.keys = keyframe_z.ToArray();
            clip.SetCurve(GenerateBone.RelativeBoneName[i], typeof(Transform), "m_LocalRotation.z", curve_z);
            clip.wrapMode = (move == Movement.RUN) ? WrapMode.Loop : WrapMode.Once;
        }
                
        {
            float[,] part_move = (move == Movement.RUN) ? base_pos_run : ((move == Movement.JUMP) ? base_pos_jump : base_pos_roll);
            AnimationCurve curve_x = new AnimationCurve();
            AnimationCurve curve_y = new AnimationCurve();
            AnimationCurve curve_z = new AnimationCurve();
            List<Keyframe> keyframe_x = new List<Keyframe>();
            List<Keyframe> keyframe_y = new List<Keyframe>();
            List<Keyframe> keyframe_z = new List<Keyframe>();
            for (int j = 0; j < part_move.GetLength(0); j++)
            {
                keyframe_x.Add(new Keyframe(j / speed, part_move[j, 0]));
                keyframe_y.Add(new Keyframe(j / speed, part_move[j, 1]));
                keyframe_z.Add(new Keyframe(j / speed, part_move[j, 2]));
            }
            curve_x.keys = keyframe_x.ToArray();
            clip.SetCurve("body_base", typeof(Transform), "m_LocalPosition.x", curve_x);
            curve_y.keys = keyframe_y.ToArray();
            clip.SetCurve("body_base", typeof(Transform), "m_LocalPosition.y", curve_y);
            curve_z.keys = keyframe_z.ToArray();
            clip.SetCurve("body_base", typeof(Transform), "m_LocalPosition.z", curve_z);
        }

        return clip;
    }
